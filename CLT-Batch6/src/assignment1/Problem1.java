public class Problem1 {

	public static void main(String[] args) {
		
		int i,j;
		// printing the rows, incremental
		for(i=0; i<5; i++) 
		{
			// printing the columns, incremental
			for(j=0; j<i; j++)
			{
				// printing the stars
				System.out.print("* ");
			}
			System.out.println();
		}
		}
		}


